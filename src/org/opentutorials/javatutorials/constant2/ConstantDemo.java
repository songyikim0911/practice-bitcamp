package org.opentutorials.javatutorials.constant2;

public class ConstantDemo {

  /*1.사과
   * 2.복숭아
   * 3.바나나
   */

  public static void main(String[] args) {
    int type = 1;//사과라는 의미
    switch(type) {
      case 1:
        System.out.println(57);//57칼로리라는의미..
        break;
      case 2:
        System.out.println(34);
        break;
      case 3:
        System.out.println(93);
        break;
    }



  }

}
